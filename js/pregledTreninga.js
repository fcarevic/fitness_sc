function languageSwitch() {
    switch (sessionStorage.getItem("tip")) {
        case 'Pilates':
            if (localStorage.getItem('lang') == 'en') {
                baza = pilates_en;
            } else
                baza = pilates;
            break;
        case 'Yoga':
        case 'Joga':
            if (localStorage.getItem('lang') == 'en') {
                baza = yoga_en;
            } else
                baza = yoga;
            break;
        case 'Cardio':
        case 'Kardio':
            if (localStorage.getItem('lang') == 'en') {
                baza = cardio_en;
            } else baza = cardio;
            break;
        case 'Core':
        case 'Kore':
            if (localStorage.getItem('lang') == 'en') {
                baza = core_en;
            } else
                baza = core;
            break;
    }

    let trtext = 'Trajanje :';
    let teztext = 'Tezina :';
    let kontakt = srpski_kontakt;
    let sorttext = 'Sortiraj po :'
    let dugmeVise = "Više...";

    jezik=jezik_srp;
    if (localStorage.getItem('lang') == 'en') {
        trtext = "Duration :"
        teztext = "Level :"
        kontakt = englski_kontakt;
        sorttext = "Sort by";
        dugmeVise='More...';
        jezik=jezik_eng;
    }

    $(".trajanjeTekst").text(trtext);
    $(".tezinaTekst").text(teztext);



    $("#contactUs h2").text(kontakt[0]);
    $("#contactUs p").text(kontakt[1]);
    $("#sortirajTekst").text(sorttext);
    document.getElementById('0').value=dugmeVise;
    document.getElementById('1').value=dugmeVise;
    document.getElementById('2').value=dugmeVise;
   
    let broadcumb1= sessionStorage.getItem("tip");
    $("#link1").text(broadcumb1);
    $("#lang").text(jezik);
}




$(document).ready(function () {

    ///////////////////////////////////////
    $("li img").click(function () {
        switch (this.id) {
            case "naziv": sortByName(); break;
            case "trajanje": sortByDuration(); break;
            case "tezina": sortByQuality(); break;
        }

    });


    $(".card input").click(function () {

        let ind = parseInt(this.id);
        sessionStorage.setItem("kategorija", baza[ind].naslov);
        loadPage("prikazKategorije");

    });
    languageSwitch();
    update();

    $(".okvir").css({ "display": "initial" });

    $("#lang").click(function(){
        if(localStorage.getItem('lang')=='en')
        localStorage.setItem('lang', 'rs');
        else localStorage.setItem('lang','en');
        prevediSesiju();
        languageSwitch();
        update();
    })





});


function sortByName() {
    baza.sort(function (a, b) { return a.naslov > b.naslov ? 1 : ((a.naslov < b.naslov) ? -1 : 0) });
    update();
}
function sortByQuality() {
    baza.sort(function (a, b) { return a.tezina - b.tezina });
    update();
}
function sortByDuration() {
    baza.sort(function (a, b) { return a.trajanje - b.trajanje });
    update();
}
function update() {
    let naslovi = document.getElementsByClassName("card-header");
    let opisi = document.getElementsByClassName("card-text");
    let slike = document.getElementsByClassName("card-img-top");

    let trajanja = document.getElementsByClassName("trajanje");
    for (let i = 0; i < naslovi.length; i++) {
        naslovi[i].innerHTML = baza[i].naslov;
        // opisi[i].innerHTML=baza[i].opis;
        slike[i].src = baza[i].slika;


        for (let j = 0; j < baza[i].tezina; j++) {

            $("#trophy" + (i * 5 + j)).removeClass("grey");
            $("#trophy" + (i * 5 + j)).css({ "color": "gold" });

        }
        trajanja[i].innerHTML = " " + baza[i].trajanje + "<sup>min<sup>";
    }


}

function prevediSesiju(){
    
    let tip = null;
     

      switch(sessionStorage.getItem('tip')){
          case 'Joga' : tip = 'Yoga'; break;
          case 'Yoga' : tip = 'Joga'; break;
          case 'Cardio': tip = 'Kardio'; break;
          case 'Kardio': tip= 'Cardio'; break;

      }
      if(tip!=null)
      sessionStorage.setItem('tip',tip );
     
}
